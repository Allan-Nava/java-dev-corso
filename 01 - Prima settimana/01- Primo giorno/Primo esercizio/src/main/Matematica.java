package main;

public class Matematica 
{
	// Matematica è una classe d'avvio
	public static void main(String[] args) 
	{
		// Stampa messaggio di benvenuto
		System.out.println("Benvenuto nella classe di matematica");
		
		// Stampa un esempio di somma
		System.out.println("Esempio di somma: 10 + 7= " + (10 + 7));
		
		// Stampa un esempio di sottrazione
		System.out.println("Esempio di sottrazione: 10 - 7= " + (10 - 7));
		
		// Stampa un esempio di divisione
		System.out.println("Esempio di divisione: 10 / 7= " + (10 / 7.0));
		
		// Stampa un esempio di moltiplicazione
		System.out.println("Esempio di moltiplicazioene: 10 x 7= " + (10 * 7.0));
		
		// Stampa un esempio divisione con double
		System.out.println("Esempio di divisione con double: 100 / 10.0= " + (100 / 10.0));
	}
}

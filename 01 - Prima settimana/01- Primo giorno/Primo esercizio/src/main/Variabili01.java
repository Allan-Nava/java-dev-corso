package main;

public class Variabili01 
{
	public static void main(String[] args) 
	{
		// Una variabile è una scatola in cui è possibile inserire un tipo di
		// elemento alla volta.
		// Proviamo a creare una variabile di tipo String che chiamiamo testo.
		String testo;
		// Finora abbiamo solo dichiarato l'esistenza di un contenitore che si
		// chiama testo e che potrà contenere solo elementi di tipo String
		// Le variabili sempre con lettera minuscola
		testo = "Ciao mondo!";
		// Questa operazione è l'INIZIALIZZAZIONE, serve a creare la variabile
		
		System.out.println(testo);
	}
}
